char	*ft_rot42(char *str)
{
	char *ret;

	ret = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = (*str - 'a' + 13) % 25 + 'a';
		else if (*str >= 'A' && *str <= 'Z')
			*str = (*str - 'A' + 13) % 25 + 'A';
		str++;
	}
	return (ret);
}
