int		ft_is_sort(int *arr, int len, int (*f)(int, int))
{
	int i;

	if (len < 2)
		return (1);
	i = 0;
	while (i < len - 1)
	{
		if ((*f)(arr[i], arr[i + 1]) > 0)
			return (0);
		i++;
	}
	return (1);
}
