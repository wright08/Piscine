#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int i;

	if (min >= max)
	{
		*range = (void *)0;
		return (0);
	}
	if ((*range = malloc((max - min) * sizeof(int))) == NULL)
		return (0);
	i = 0;
	while (min + i < max)
	{
		(*range)[i] = min + i;
		i++;
	}
	return (max - min);
}
