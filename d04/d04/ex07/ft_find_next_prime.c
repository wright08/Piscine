int		ft_find_next_prime(int n)
{
	int	i;
	int j;

	if (n < 2)
		return (2);
	i = n;
	while (i > 0)
	{
		j = 2;
		while (j * j <= i && i % j != 0)
			j++;
		if (j * j > i)
			return (i);
		i++;
	}
	return (n);
}
