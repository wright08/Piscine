int		ft_atoi(char *str)
{
	int ret;
	int neg;

	neg = *str == '-';
	if (neg)
	{
		str++;
	}
	ret = 0;
	while ('0' <= *str && *str <= '9')
	{
		ret = ret * 10 + *(str++) - '0';
	}
	return (neg ? -ret : ret);
}
